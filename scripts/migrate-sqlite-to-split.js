#!/usr/bin/env node
/**
 * Migrates the current sql.js podium.db into the split production stores:
 * - PostgreSQL: users, friend_requests, attendance
 * - MongoDB/Cosmos Mongo API: theatres, shows
 *
 * Required env:
 *   DATABASE_URL=postgres://...
 *   NOSQL_CONNECTION_STRING=mongodb://...
 *   NOSQL_DB_NAME=podium
 *
 * Usage:
 *   node scripts/migrate-sqlite-to-split.js --dry-run
 *   node scripts/migrate-sqlite-to-split.js
 */

'use strict';

const fs = require('fs');
const path = require('path');

// Load environment variables from the server's .env file
require('dotenv').config({ path: path.join(__dirname, '..', 'Podium App', 'server', '.env') });

const { MongoClient } = require('mongodb');
const { Pool } = require('pg');

const args = process.argv.slice(2);
const DRY_RUN = args.includes('--dry-run');
const SERVER_DIR = path.resolve(__dirname, '..', 'Podium App', 'server');
const DB_FILE = process.env.DB_PATH
  ? path.resolve(SERVER_DIR, process.env.DB_PATH)
  : path.join(SERVER_DIR, 'podium.db');
const SQL_JS_DIR = path.join(SERVER_DIR, 'node_modules', 'sql.js');
const DATABASE_URL = process.env.DATABASE_URL || process.env.POSTGRES_URL || '';
const NOSQL_CONNECTION_STRING = process.env.NOSQL_CONNECTION_STRING || process.env.MONGODB_URI || '';
const NOSQL_DB_NAME = process.env.NOSQL_DB_NAME || process.env.MONGODB_DB_NAME || 'podium';

function log(message) {
  console.log(`[${new Date().toISOString()}] ${message}`);
}

function postgresSslConfig() {
  const sslMode = String(process.env.POSTGRES_SSL || process.env.PGSSLMODE || '').toLowerCase();
  if (sslMode === 'true' || sslMode === 'require') {
    return { rejectUnauthorized: false };
  }
  return undefined;
}

async function openSqlite() {
  const initSqlJs = require(path.join(SQL_JS_DIR, 'dist', 'sql-asm.js'));
  const SQL = await initSqlJs({
    locateFile: () => path.join(SQL_JS_DIR, 'dist', 'sql-asm-memory-growth.js'),
  });
  const buffer = fs.readFileSync(DB_FILE);
  return new SQL.Database(buffer);
}

function queryAll(db, sql, params = []) {
  const stmt = db.prepare(sql);
  stmt.bind(params);
  const rows = [];
  while (stmt.step()) rows.push(stmt.getAsObject());
  stmt.free();
  return rows;
}

async function ensurePostgresSchema(pool) {
  await pool.query(`
    CREATE TABLE IF NOT EXISTS users (
      id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
      email TEXT UNIQUE NOT NULL,
      password_hash TEXT NOT NULL,
      name TEXT NOT NULL,
      avatar TEXT DEFAULT '',
      bio TEXT DEFAULT '',
      city TEXT DEFAULT '',
      created_at TIMESTAMPTZ DEFAULT CURRENT_TIMESTAMP
    )
  `);

  await pool.query(`
    CREATE TABLE IF NOT EXISTS attendance (
      id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
      user_id INTEGER NOT NULL REFERENCES users(id) ON DELETE CASCADE,
      performance_id INTEGER,
      show_id TEXT NOT NULL,
      title_snapshot TEXT DEFAULT '',
      date_time_snapshot TIMESTAMPTZ,
      theatre_name_snapshot TEXT DEFAULT '',
      theatre_city_snapshot TEXT DEFAULT '',
      created_at TIMESTAMPTZ DEFAULT CURRENT_TIMESTAMP,
      UNIQUE(user_id, show_id)
    )
  `);

  await pool.query(`
    CREATE TABLE IF NOT EXISTS friend_requests (
      id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
      from_user_id INTEGER NOT NULL REFERENCES users(id) ON DELETE CASCADE,
      to_user_id INTEGER NOT NULL REFERENCES users(id) ON DELETE CASCADE,
      status TEXT DEFAULT 'pending' CHECK(status IN ('pending', 'accepted', 'rejected')),
      created_at TIMESTAMPTZ DEFAULT CURRENT_TIMESTAMP,
      UNIQUE(from_user_id, to_user_id)
    )
  `);

  await pool.query('CREATE INDEX IF NOT EXISTS idx_attendance_user ON attendance(user_id)');
  await pool.query('CREATE INDEX IF NOT EXISTS idx_attendance_show ON attendance(show_id)');
  await pool.query('CREATE INDEX IF NOT EXISTS idx_friend_requests_from ON friend_requests(from_user_id)');
  await pool.query('CREATE INDEX IF NOT EXISTS idx_friend_requests_to ON friend_requests(to_user_id)');
}

async function ensureMongoIndexes(mongoDb) {
  await mongoDb.collection('theatres').createIndex({ stable_id: 1 }, { unique: true });
  await mongoDb.collection('theatres').createIndex({ osm_id: 1 });
  await mongoDb.collection('shows').createIndex({ show_id: 1 }, { unique: true });
  await mongoDb.collection('shows').createIndex({ theatre_id: 1 });
  await mongoDb.collection('shows').createIndex({ date_time: 1 });
  await mongoDb.collection('shows').createIndex({ removed: 1 });
  await mongoDb.collection('shows').createIndex({ status: 1 });
}

function cleanDate(value) {
  return value || null;
}

async function resetIdentitySequence(pool, tableName) {
  await pool.query(
    `SELECT setval(pg_get_serial_sequence($1, 'id'), COALESCE((SELECT MAX(id) FROM ${tableName}), 1), true)`,
    [tableName]
  );
}

async function migratePostgres(pool, db) {
  const users = queryAll(db, 'SELECT * FROM users');
  const friendRequests = queryAll(db, 'SELECT * FROM friend_requests');
  const attendance = queryAll(
    db,
    `SELECT a.*, p.show_id as performance_show_id, p.title, p.date_time, t.name as theatre_name, t.city as theatre_city
     FROM attendance a
     JOIN performances p ON a.performance_id = p.id
     JOIN theatres t ON p.theatre_id = t.id`
  );

  if (DRY_RUN) {
    return { users: users.length, friendRequests: friendRequests.length, attendance: attendance.length };
  }

  const client = await pool.connect();
  try {
    await client.query('BEGIN');

    for (const user of users) {
      await client.query(
        `INSERT INTO users (id, email, password_hash, name, avatar, bio, city, created_at)
         VALUES ($1, $2, $3, $4, $5, $6, $7, COALESCE($8::timestamptz, CURRENT_TIMESTAMP))
         ON CONFLICT (id) DO UPDATE SET
           email = EXCLUDED.email,
           password_hash = EXCLUDED.password_hash,
           name = EXCLUDED.name,
           avatar = EXCLUDED.avatar,
           bio = EXCLUDED.bio,
           city = EXCLUDED.city`,
        [user.id, user.email, user.password_hash, user.name, user.avatar || '', user.bio || '', user.city || '', user.created_at]
      );
    }

    for (const request of friendRequests) {
      await client.query(
        `INSERT INTO friend_requests (id, from_user_id, to_user_id, status, created_at)
         VALUES ($1, $2, $3, $4, COALESCE($5::timestamptz, CURRENT_TIMESTAMP))
         ON CONFLICT (id) DO UPDATE SET
           from_user_id = EXCLUDED.from_user_id,
           to_user_id = EXCLUDED.to_user_id,
           status = EXCLUDED.status,
           created_at = EXCLUDED.created_at`,
        [request.id, request.from_user_id, request.to_user_id, request.status, request.created_at]
      );
    }

    for (const row of attendance) {
      const showId = row.show_id || row.performance_show_id || String(row.performance_id);
      await client.query(
        `INSERT INTO attendance (
          id, user_id, performance_id, show_id, title_snapshot, date_time_snapshot,
          theatre_name_snapshot, theatre_city_snapshot, created_at
        )
         VALUES ($1, $2, $3, $4, $5, $6::timestamptz, $7, $8, COALESCE($9::timestamptz, CURRENT_TIMESTAMP))
         ON CONFLICT (id) DO UPDATE SET
           user_id = EXCLUDED.user_id,
           performance_id = EXCLUDED.performance_id,
           show_id = EXCLUDED.show_id,
           title_snapshot = EXCLUDED.title_snapshot,
           date_time_snapshot = EXCLUDED.date_time_snapshot,
           theatre_name_snapshot = EXCLUDED.theatre_name_snapshot,
           theatre_city_snapshot = EXCLUDED.theatre_city_snapshot,
           created_at = EXCLUDED.created_at`,
        [
          row.id,
          row.user_id,
          row.performance_id,
          showId,
          row.title_snapshot || row.title || '',
          cleanDate(row.date_time_snapshot || row.date_time),
          row.theatre_name_snapshot || row.theatre_name || '',
          row.theatre_city_snapshot || row.theatre_city || '',
          row.created_at,
        ]
      );
    }

    await resetIdentitySequence(client, 'users');
    await resetIdentitySequence(client, 'friend_requests');
    await resetIdentitySequence(client, 'attendance');
    await client.query('COMMIT');
  } catch (err) {
    await client.query('ROLLBACK');
    throw err;
  } finally {
    client.release();
  }

  return { users: users.length, friendRequests: friendRequests.length, attendance: attendance.length };
}

async function migrateMongo(mongoDb, db) {
  const theatreRows = queryAll(db, 'SELECT * FROM theatres');
  const showRows = queryAll(
    db,
    `SELECT p.*, t.stable_id as theatre_stable_id, t.id as theatre_numeric_id,
      t.name as theatre_name, t.city as theatre_city, t.address as theatre_address,
      t.province as theatre_province
     FROM performances p
     JOIN theatres t ON p.theatre_id = t.id`
  );

  if (DRY_RUN) {
    return { theatres: theatreRows.length, shows: showRows.length };
  }

  const theatres = mongoDb.collection('theatres');
  const shows = mongoDb.collection('shows');
  const now = new Date().toISOString();

  for (const theatre of theatreRows) {
    const stableId = theatre.stable_id || String(theatre.id);
    const doc = {
      _id: stableId,
      id: stableId,
      numeric_id: theatre.id,
      stable_id: stableId,
      osm_id: theatre.osm_id || '',
      name: theatre.name,
      city: theatre.city,
      address: theatre.address || '',
      province: theatre.province || '',
      image_url: theatre.image_url || '',
      website: theatre.website || '',
      description: theatre.description || '',
      latitude: theatre.latitude,
      longitude: theatre.longitude,
      migrated_at: now,
    };

    await theatres.updateOne(
      { stable_id: stableId },
      { $set: doc, $setOnInsert: { created_at: now } },
      { upsert: true }
    );
  }

  for (const show of showRows) {
    const showId = show.show_id || String(show.id);
    const doc = {
      _id: showId,
      id: showId,
      numeric_id: show.id,
      show_id: showId,
      title: show.title,
      description: show.description || '',
      genre: show.genre || '',
      date_time: show.date_time,
      theatre_id: show.theatre_stable_id || String(show.theatre_id),
      theatre_numeric_id: show.theatre_numeric_id,
      theatre_name: show.theatre_name,
      theatre_city: show.theatre_city,
      theatre_address: show.theatre_address || '',
      theatre_province: show.theatre_province || '',
      ticket_url: show.ticket_url || '',
      image_url: show.image_url || '',
      source_event_id: show.source_event_id || '',
      source_url: show.source_url || '',
      content_hash: show.content_hash || '',
      status: show.status || 'active',
      removed: !!show.removed,
      removed_when: show.removed_when || null,
      changed_at: show.changed_at || null,
      first_seen_at: show.first_seen_at || now,
      last_seen_at: show.last_seen_at || null,
      missing_since: show.missing_since || null,
      missing_count: show.missing_count || 0,
      migrated_at: now,
    };

    await shows.updateOne(
      { show_id: showId },
      { $set: doc, $setOnInsert: { created_at: now } },
      { upsert: true }
    );
  }

  return { theatres: theatreRows.length, shows: showRows.length };
}

async function main() {
  console.log('\nPodium SQLite -> Split Store Migrator');
  console.log('------------------------------------------------------------');
  if (DRY_RUN) log('DRY-RUN mode - no changes will be written');

  if (!DATABASE_URL) throw new Error('DATABASE_URL or POSTGRES_URL is required.');
  if (!NOSQL_CONNECTION_STRING) throw new Error('NOSQL_CONNECTION_STRING or MONGODB_URI is required.');
  if (!fs.existsSync(DB_FILE)) throw new Error(`${DB_FILE} not found.`);

  const sqliteDb = await openSqlite();
  const pool = new Pool({ connectionString: DATABASE_URL, ssl: postgresSslConfig() });
  const mongoClient = new MongoClient(NOSQL_CONNECTION_STRING);

  try {
    await ensurePostgresSchema(pool);
    await mongoClient.connect();
    const mongoDb = mongoClient.db(NOSQL_DB_NAME);
    await ensureMongoIndexes(mongoDb);

    const pgResult = await migratePostgres(pool, sqliteDb);
    const mongoResult = await migrateMongo(mongoDb, sqliteDb);

    console.log('\nSummary:');
    console.log(`  PostgreSQL users          : ${pgResult.users}`);
    console.log(`  PostgreSQL friend requests: ${pgResult.friendRequests}`);
    console.log(`  PostgreSQL attendance     : ${pgResult.attendance}`);
    console.log(`  NoSQL theatres            : ${mongoResult.theatres}`);
    console.log(`  NoSQL shows               : ${mongoResult.shows}`);
    console.log('');
    log(DRY_RUN ? 'Dry run complete - no changes written' : 'Migration complete');
  } finally {
    await pool.end();
    await mongoClient.close();
  }
}

main().catch((err) => {
  console.error('\nFatal error:', err.message);
  process.exit(1);
});
